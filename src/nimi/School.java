    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed

        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Please select a row to update.");
            return;
        }

        int studentId = Integer.parseInt(jTable1.getValueAt(selectedRow, 0).toString());

        String name = nem.getText().trim();
        String ageText = edad.getText().trim();
        String address = add.getText().trim();
        String contact = num.getText().trim();
        String college = ledge.getSelectedItem().toString();
        String program = gram.getSelectedItem().toString();
        String gender = "";

        if (jRadioButton1.isSelected()) {
            gender = "Male";
        } else if (jRadioButton2.isSelected()) {
            gender = "Female";
        }

// Parse age (set to 0 if empty or invalid)
        int age = 0;
        try {
            age = Integer.parseInt(ageText);
        } catch (NumberFormatException e) {
            // leave age as 0
        }

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydata?useSSL=false", "root", "");

            String sql = "UPDATE dateta SET snam=?, edz=?, genda=?, ress=?, nmber=?, kalids=?, prgram=? WHERE id=?";
            PreparedStatement pst = con.prepareStatement(sql);

            pst.setString(1, name);
            pst.setInt(2, age);
            pst.setString(3, gender);
            pst.setString(4, address);
            pst.setString(5, contact);
            pst.setString(6, college);
            pst.setString(7, program);
            pst.setInt(8, studentId);

            pst.executeUpdate();

            JOptionPane.showMessageDialog(null, "Data updated successfully!");

            // Update JTable
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setValueAt(name, selectedRow, 1);
            model.setValueAt(age, selectedRow, 2);
            model.setValueAt(gender, selectedRow, 3);
            model.setValueAt(address, selectedRow, 4);
            model.setValueAt(contact, selectedRow, 5);
            model.setValueAt(college, selectedRow, 6);
            model.setValueAt(program, selectedRow, 7);

            con.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }

    }//GEN-LAST:event_jButton7ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Please select a row to delete.");
            return;
        }

// Get student_id from JTable (assuming it's in column 0)
        int studentId = Integer.parseInt(jTable1.getValueAt(selectedRow, 0).toString());

        int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this record?", "Confirm Delete", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydata?useSSL=false", "root", "");

                String sql = "DELETE FROM dateta WHERE id=?";
                PreparedStatement pst = con.prepareStatement(sql);
                pst.setInt(1, studentId);
                pst.executeUpdate();

                // Remove from JTable
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.removeRow(selectedRow);

                JOptionPane.showMessageDialog(null, "Record deleted successfully!");

                con.close();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

