    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed

        // Clear existing rows in JTable before displaying new data
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);  // This removes all existing rows in the JTable

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydata?useSSL=false", "root", "");

            String sql = "SELECT * FROM dateta";  // Select all data from the table
            PreparedStatement pst = con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                // Assuming your columns in JTable are: id, name, age, gender, address, contact, college, program
                Object[] row = new Object[8];
                row[0] = rs.getInt("id");  // student_id
                row[1] = rs.getString("snam");     // name
                row[2] = rs.getInt("edz");         // age
                row[3] = rs.getString("genda");    // gender
                row[4] = rs.getString("ress");     // address
                row[5] = rs.getString("nmber");    // contact
                row[6] = rs.getString("kalids");   // college
                row[7] = rs.getString("prgram");   // program

                model.addRow(row);  // Add the new row to the JTable
            }

            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }


    }//GEN-LAST:event_jButton9ActionPerformed

